package kr.co.airbnb.mvc.dto;

public class AopVO {
	private int hno;
	private int uno;
	private int resno;
	private String uaddressdo;
	private String resdate;
	private String resprice;
	private String searchIn;
	private String searchOut;
	
	public String getSearchIn() {
		return searchIn;
	}
	public void setSearchIn(String searchIn) {
		this.searchIn = searchIn;
	}
	public String getSearchOut() {
		return searchOut;
	}
	public void setSearchOut(String searchOut) {
		this.searchOut = searchOut;
	}
	public int getHno() {
		return hno;
	}
	public void setHno(int hno) {
		this.hno = hno;
	}
	public int getUno() {
		return uno;
	}
	public void setUno(int uno) {
		this.uno = uno;
	}
	public int getResno() {
		return resno;
	}
	public void setResno(int resno) {
		this.resno = resno;
	}
	public String getUaddressdo() {
		return uaddressdo;
	}
	public void setUaddressdo(String uaddressdo) {
		this.uaddressdo = uaddressdo;
	}
	public String getResdate() {
		return resdate;
	}
	public void setResdate(String resdate) {
		this.resdate = resdate;
	}
	public String getResprice() {
		return resprice;
	}
	public void setResprice(String resprice) {
		this.resprice = resprice;
	}

}
